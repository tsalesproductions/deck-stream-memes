deckManager = {
    target: null,
    createDiv: function(item){
        let div = document.createElement("div");
            div.classList.add("item");

        div.addEventListener("click", function(){
            let audio = new Audio(item.src);
                audio.currentTime = 0;
                audio.play();
        });

        let span = document.createElement("span");
            span.innerText = item.nome;

        div.append(span);

        this.target.append(div);
    },
    listAudio: function(){
        audios.map(function(item){
            deckManager.createDiv(item); 
        });
    },
    mappingKeyBoard: function(event){
        audios.map(function(item){
            let depurator = document.querySelector(".keyPress");
            if(event.code == item.keyCode){
                let audio = new Audio(item.src);
                    audio.currentTime = 0;
                    audio.play();
            }
            
            depurator.innerText = event.code;
        });
    },
    init: function(){
        this.target = document.querySelector(".container");
        this.listAudio();
    }
}

window.addEventListener("load", deckManager.init());

document.addEventListener("keypress", (event) => deckManager.mappingKeyBoard(event));